program example

    use pm_kind, only: SK, IK, LK ! all intrinsic types and kinds are supported.
    use pm_container, only: css_pdt
    use pm_arraySort, only: isAscending
    use pm_io, only: display_type

    implicit none

    type(display_type) :: disp

    disp = display_type(file = "main.out.F90")

    call disp%skip()
    call disp%show("isAscending('')")
    call disp%show( isAscending('') )
    call disp%skip()
    call disp%show("isAscending('?Aa')")
    call disp%show( isAscending('?Aa') )
    call disp%skip()
    call disp%show("isAscending('A!b')")
    call disp%show( isAscending('A!b') )
    call disp%skip()
    call disp%show("isAscending('zxw')")
    call disp%show( isAscending('zxw') )
    call disp%skip()
    call disp%show("isAscending('AAA')")
    call disp%show( isAscending('AAA') )
    call disp%skip()

    call disp%skip()
    call disp%show("isAscending([character(0) ::])")
    call disp%show( isAscending([character(0) ::]) )
    call disp%skip()
    call disp%show("isAscending(['?', 'A', 'a'])")
    call disp%show( isAscending(['?', 'A', 'a']) )
    call disp%skip()
    call disp%show("isAscending(['A', '!', 'b'])")
    call disp%show( isAscending(['A', '!', 'b']) )
    call disp%skip()
    call disp%show("isAscending(['z', 'x', 'w'])")
    call disp%show( isAscending(['z', 'x', 'w']) )
    call disp%skip()
    call disp%show("isAscending(['A', 'A', 'A'])")
    call disp%show( isAscending(['A', 'A', 'A']) )
    call disp%skip()

    call disp%skip()
    call disp%show("isAscending([integer ::])")
    call disp%show( isAscending([integer ::]) )
    call disp%skip()
    call disp%show("isAscending([+1, +2, +3])")
    call disp%show( isAscending([+1, +2, +3]) )
    call disp%skip()
    call disp%show("isAscending([-1, -2, -3])")
    call disp%show( isAscending([-1, -2, -3]) )
    call disp%skip()
    call disp%show("isAscending([-1, +2, -3])")
    call disp%show( isAscending([-1, +2, -3]) )
    call disp%skip()
    call disp%show("isAscending([-1, -1, -1])")
    call disp%show( isAscending([-1, -1, -1]) )
    call disp%skip()

    call disp%skip()
    call disp%show("isAscending([logical ::])")
    call disp%show( isAscending([logical ::]) )
    call disp%skip()
    call disp%show("isAscending([.false., .false., .true.])")
    call disp%show( isAscending([.false., .false., .true.]) )
    call disp%skip()
    call disp%show("isAscending([.true., .false., .false.])")
    call disp%show( isAscending([.true., .false., .false.]) )
    call disp%skip()
    call disp%show("isAscending([.false., .true., .false.])")
    call disp%show( isAscending([.false., .true., .false.]) )
    call disp%skip()
    call disp%show("isAscending([.true., .true., .true.])")
    call disp%show( isAscending([.true., .true., .true.]) )
    call disp%skip()

    call disp%skip()
    call disp%show("isAscending([complex ::])")
    call disp%show( isAscending([complex ::]) )
    call disp%skip()
    call disp%show("isAscending([(+1., -1.), (+2., -2.), (+3., -3.)])")
    call disp%show( isAscending([(+1., -1.), (+2., -2.), (+3., -3.)]) )
    call disp%skip()
    call disp%show("isAscending([(-1., +1.), (-2., +2.), (-3., +3.)])")
    call disp%show( isAscending([(-1., +1.), (-2., +2.), (-3., +3.)]) )
    call disp%skip()
    call disp%show("isAscending([(-1., +1.), (+2., -2.), (-3., +3.)])")
    call disp%show( isAscending([(-1., +1.), (+2., -2.), (-3., +3.)]) )
    call disp%skip()
    call disp%show("isAscending([(-1., +1.), (-1., +1.), (-1., +1.)])")
    call disp%show( isAscending([(-1., +1.), (-1., +1.), (-1., +1.)]) )
    call disp%skip()
    call disp%show("isAscending([(-1., +1.), (-1., -2.), (-1., +3.)])")
    call disp%show( isAscending([(-1., +1.), (-1., -2.), (-1., +3.)]) )
    call disp%skip()

    call disp%skip()
    call disp%show("isAscending([real ::])")
    call disp%show( isAscending([real ::]) )
    call disp%skip()
    call disp%show("isAscending([+1., +2., +3.])")
    call disp%show( isAscending([+1., +2., +3.]) )
    call disp%skip()
    call disp%show("isAscending([-1., -2., -3.])")
    call disp%show( isAscending([-1., -2., -3.]) )
    call disp%skip()
    call disp%show("isAscending([-1., +2., -3.])")
    call disp%show( isAscending([-1., +2., -3.]) )
    call disp%skip()
    call disp%show("isAscending([-1., -1., -1.])")
    call disp%show( isAscending([-1., -1., -1.]) )
    call disp%skip()

#if !__GFORTRAN__
    !   \bug
    !   Intel ifort 2022.3 cannot handle aliases for pdt names (e.g., strc => css_pdt).
    call disp%skip()
    call disp%show("isAscending([css_pdt('is'), css_pdt(  'sorted'), css_pdt('string')])")
    call disp%show( isAscending([css_pdt('is'), css_pdt(  'sorted'), css_pdt('string')]) )
    call disp%skip()
    call disp%show("isAscending([css_pdt('string'), css_pdt('is'), css_pdt('sorted')])")
    call disp%show( isAscending([css_pdt('string'), css_pdt('is'), css_pdt('sorted')]) )
    call disp%skip()
    call disp%show("isAscending([css_pdt('same string'), css_pdt('same '), css_pdt('same')])")
    call disp%show( isAscending([css_pdt('same string'), css_pdt('same '), css_pdt('same')]) )
    call disp%skip()
#endif

end program example