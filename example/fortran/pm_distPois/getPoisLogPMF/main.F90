program example

    use pm_kind, only: SK, IK, LK
    use pm_kind, only: RKC => RK ! all processor kinds are supported.
    use pm_io, only: display_type
    use pm_distPois, only: getPoisLogPMF

    implicit none

    type(display_type) :: disp
    disp = display_type(file = "main.out.F90")

    call disp%skip()
    call disp%show("getPoisLogPMF(0_IK, lambda = 2._RKC)")
    call disp%show( getPoisLogPMF(0_IK, lambda = 2._RKC) )
    call disp%skip()

    call disp%skip()
    call disp%show("getPoisLogPMF(1_IK, lambda = 2._RKC)")
    call disp%show( getPoisLogPMF(1_IK, lambda = 2._RKC) )
    call disp%skip()

    call disp%skip()
    call disp%show("getPoisLogPMF(2_IK, lambda = 2._RKC)")
    call disp%show( getPoisLogPMF(2_IK, lambda = 2._RKC) )
    call disp%skip()

    call disp%skip()
    call disp%show("getPoisLogPMF([integer(IK) :: 0, 1, 2], lambda = 2._RKC)")
    call disp%show( getPoisLogPMF([integer(IK) :: 0, 1, 2], lambda = 2._RKC) )
    call disp%skip()

    call disp%skip()
    call disp%show("getPoisLogPMF([integer(IK) :: 0, 1, 2], lambda = [0.1_RKC, 1._RKC, 10._RKC])")
    call disp%show( getPoisLogPMF([integer(IK) :: 0, 1, 2], lambda = [0.1_RKC, 1._RKC, 10._RKC]) )
    call disp%skip()

    !%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
    ! Output an example array for visualization.
    !%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

    block

        use pm_arrayRange, only: getRange
        integer(IK), allocatable :: count(:)
        integer :: fileUnit, i

        count = getRange(0_IK, 20_IK)
        open(newunit = fileUnit, file = "getPoisLogPMF.IK.txt")
        do i = 1, size(count)
            write(fileUnit, "(*(g0,:,' '))" ) count(i), exp(getPoisLogPMF(count(i), [.1_RKC, 1._RKC, 4._RKC, 10._RKC]))
        end do
        close(fileUnit)

    end block

end program example