    call disp%show( root )
    call disp%show("getPolyVal(coef, root)")
    call disp%show( getPolyVal(coef, root) )
    call disp%skip()
    call disp%show("root = getPolyRoot(coef%re, method = eigen) ! polynomial with real coefficients.")
                    root = getPolyRoot(coef%re, method = eigen)
